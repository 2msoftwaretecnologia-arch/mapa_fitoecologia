import sys
import os
sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '..')))

from database.text_infos import Text_infos
from buildkite.Windows.manipular_windos import WAIT
from buildkite.functions_tkinter.interfaces import find_shp_file
from buildkite.functions_pyautogui.funcoes_teclado_mouse import (click_center_screen,press_enter,press_ctrl_home,
                                                                 press_down,press_tab,write_text,press_key)
from buildkite.manipular_textos.fast_formatter import format_path                                                                 
from buildkite.functions_pyautogui.mause_complexo import click                                                                  
from buildkite.utils.helpers import find_aii,BRAKE_WINDOW
from database.requests import get_or_set_coordinate
from utils_info import tipos_de_mapa

def select_apr():
    
    var_apr = find_shp_file("escolha o shp da apr")
    var_aii = find_aii(var_apr)
    var_apr = format_path(var_apr)
    var_aii = format_path(var_aii)
    Text_infos.caminho_mapa_atual = var_apr
    layers_coordinates = get_or_set_coordinate(objeto_id=1,mensagem="Clique na lista onde fica a Layer principal do mapa")
    click_center_screen()
    WAIT(0.2)
    click(layers_coordinates[0],layers_coordinates[1])
    WAIT(0.5)
    press_ctrl_home()
    WAIT(0.5)
    press_down()
    WAIT(0.2)
    press_enter()
    WAIT(0.5)
    BRAKE_WINDOW("Espere a seleção ser concluída e aperte Entendi")
    source_cordenadas = get_or_set_coordinate(objeto_id=12,mensagem="Clique no botão 'Source'")
    click(source_cordenadas[0],source_cordenadas[1],clicks_quant=3)
    WAIT(0.3)
    press_tab(6,wait_time=0.01)
    press_enter()
    BRAKE_WINDOW("Espere a seleção ser concluída e aperte Entendi")
    WAIT(0.3)
    input_caminho = get_or_set_coordinate(14,"Clique no campo onde digitamos o caminho do shp")
    WAIT(0.3)
    click(input_caminho[0],input_caminho[1])
    write_text(var_apr,speed=0.002)
    press_enter()
    WAIT(0.2)
    press_tab(1)
    WAIT(0.2)
    press_enter()
    press_key("f2",wait_time=0.4)
    write_text(f'{Text_infos.nome_propriedade} - A.P.R',speed=0.002)
    press_enter(wait_time=0.5)
    press_down(ammount=2,wait_time=0.002)
    WAIT(0.2)
    press_enter()
    BRAKE_WINDOW("Espere a seleção ser concluída e aperte Entendi")
    click(source_cordenadas[0],source_cordenadas[1],clicks_quant=3)
    WAIT(0.5)
    press_tab(6,wait_time=0.01)
    press_enter()
    WAIT(0.5)
    write_text(var_aii,speed=0.002)
    press_enter()
    WAIT(0.2)
    press_tab()
    WAIT(0.2)
    press_enter()
    WAIT(1)
    #modificando a camada de baixo
    if Text_infos.tipo_mapa in tipos_de_mapa:
        press_down(ammount=10,wait_time=0.002)
    else:
        if Text_infos.tipo_mapa == 'Fitoecologia':
            press_down(ammount=18,wait_time=0.002)
    
    WAIT(0.2)
    press_enter()
    BRAKE_WINDOW("Espere a seleção ser concluída e aperte Entendi")
    click(source_cordenadas[0],source_cordenadas[1],clicks_quant=3)
    WAIT(0.5)
    press_tab(6,wait_time=0.01)
    press_enter()
    WAIT(1)
    write_text(var_apr,speed=0.002)
    press_enter()
    WAIT(0.2)
    press_tab(1)
    WAIT(0.2)
    press_enter()
    WAIT(0.2)
    press_key("f2",wait_time=0.4)
    write_text(f'{Text_infos.nome_propriedade} - A.P.R',speed=0.002)
    press_enter(wait_time=0.5)
    press_down(ammount=2,wait_time=0.002)
    WAIT(0.2)
    press_enter()
    BRAKE_WINDOW("Espere a seleção ser concluída e aperte Entendi")
    click(source_cordenadas[0],source_cordenadas[1],clicks_quant=3)
    WAIT(0.2)
    press_tab(6,wait_time=0.01)
    press_enter()
    WAIT(0.5)
    write_text(var_aii,speed=0.002)
    press_enter()
    WAIT(0.2)
    press_tab(1)
    WAIT(0.2)
    press_enter()
    WAIT(1)